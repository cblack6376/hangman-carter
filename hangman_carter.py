def play_again():
    restart = False
    while restart == False:
        replay = input("Do you want to play again? ")
        if replay == 'no' or replay == 'n' or replay == 'no thanks':
            restart = True
        elif replay == 'yes' or replay == 'y' or replay == 'sure':
            restart = False
            print("Ok!")
            play()
        else:
            print("What was that?")
           

def play():

    puzzle_dir = 'puzzles'
    category_file = get_category(puzzle_dir)
    word = get_puzzle(category_file)
    solved = "-" * len(word)

    correct = ""
    incorrect = ""
    strikes = 0
    limit = 6
    
    solved = check(word, solved, correct)
    display_board(solved, incorrect, strikes, word)

    while word != solved and strikes < limit:
        letter = get_guess()

        if letter not in word:
            strikes += 1
            incorrect += letter
        else:
            correct += letter

        solved = check(word, solved, correct)
        display_board(solved, incorrect, strikes, word)

    if word == solved:
        print("You win!")
    else:
        print("You lose!")


